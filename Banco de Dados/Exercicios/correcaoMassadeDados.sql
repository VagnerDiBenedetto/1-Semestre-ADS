create database exercicio;

use exercicio;

-- CRIAÇÃO DAS TABELAS

create table aluno (
ra    char(09) not null,
nome  varchar(50),
bairro  varchar(50),
primary key (ra));

create table curso (
id int not null auto_increment,
nome	varchar(20),
coordenador varchar(50),
primary key (id));

create table aluno_curso
(
fkCurso    int not null,
fkAluno    char(09) not null,
dataInicio date,
media      decimal(4,2),
nivel      smallint,
primary key(fkCurso, fkAluno),
constraint fkCurso foreign key (fkcurso) references curso(id),
constraint fkaluno foreign key (fkaluno) references aluno(ra));

